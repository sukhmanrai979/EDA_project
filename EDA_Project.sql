SELECT 'TotalCustomers' AS measure_name, COUNT(customer_id) AS measure_value FROM gold.dim_customers
UNION ALL
SELECT 'TotalCustomersOrderPlaced' AS measure_name, COUNT(DISTINCT customer_key) AS measure_value FROM gold.dim_customers


-- Magnitude Analysis
-- Comparing the measure values by categories
-- [Measure] By [Dimension]
-- e.g. Total Sales by Country
-- Total Quantity by Category

-- Total Customers by countries

SELECT country, COUNT(customer_key) TotalCustomers 
FROM gold.dim_customers
GROUP BY country
ORDER BY TotalCustomers DESC

-- Total Customers by gender

SELECT gender, COUNT(customer_key) TotalCustomers 
FROM gold.dim_customers
GROUP BY gender
ORDER BY TotalCustomers DESC

-- Total Products by category

SELECT category, COUNT(product_key) TotalProducts 
FROM gold.dim_products
GROUP BY category
ORDER BY TotalProducts DESC

-- Average Costs of Products in each category

SELECT category, AVG(cost) AvgCost
FROM gold.dim_products
GROUP BY category


-- Total Revenue generated for each category


SELECT dp.category, SUM(fs.TotalSales) TotalRevenue FROM gold.dim_products dp 
JOIN 
(SELECT product_key, SUM(sales_amount) TotalSales FROM gold.fact_sales
GROUP BY product_key) fs 
ON dp.product_key = fs.product_key
GROUP BY category

-- Total Revenue generated by each customer

SELECT dc.customer_key, dc.first_name, dc.last_name, fs.TotalSales 
FROM gold.dim_customers dc
JOIN
(SELECT customer_key, SUM(sales_amount) TotalSales FROM gold.fact_sales
GROUP BY customer_key) fs
ON dc.customer_key = fs.customer_key

-- Distribution of Sold items across Countries

SELECT dc.country, SUM(fs.TotalItems) TotalItemsSold
FROM gold.dim_customers dc
JOIN
(SELECT customer_key, SUM(quantity) TotalItems FROM gold.fact_sales
GROUP BY customer_key) fs
ON dc.customer_key = fs.customer_key
GROUP BY country


-- Ranking
-- Ranking a Dimension by a Measure
-- e.g. Ranking Countries by Total Sales
-- Top 5 Products by Quantity

-- 5 Products that generate the highest revenue

SELECT TOP 5 p.product_key, p.product_name, SUM(sales_amount) TotalRevenue FROM gold.fact_sales f
JOIN gold.dim_products p
ON f.product_key = p.product_key
GROUP BY p.product_key, p.product_name
ORDER BY TotalRevenue DESC


-- Using Window Function
SELECT * FROM
(
    SELECT
    p.product_name, 
    SUM(sales_amount) TotalRevenue,
    ROW_NUMBER() OVER(ORDER BY SUM(sales_amount) DESC) Rank 
    FROM gold.fact_sales f
    JOIN gold.dim_products p
    ON f.product_key = p.product_key
    GROUP BY p.product_name
)t WHERE Rank <= 5



-- 5 worst-performing products in terms of sales

