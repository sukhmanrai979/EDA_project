-- EDA Project

-- Dimension vs Measures

-- If data type of a column is not numeric (e.g. String or Date)
-- then it is a DIMENSION

-- If data type of a column is numeric 
-- Then we ask another question: Does it make sense to aggregate it?
-- For example, phone numbers are numeric but it doesn't make sense to find 
-- the average of phone numbers

-- If it makes sense to aggregate, then the column is a MEASURE
-- Otherwise it is a DIMENSION

-- Dimensions are used for grouping up data (GROUP BY)
-- Measures are used for aggregating data 


-- Explore all objects in the database
SELECT * FROM INFORMATION_SCHEMA.TABLES

-- Explore all columns in the database
SELECT * FROM INFORMATION_SCHEMA.COLUMNS
WHERE TABLE_NAME = 'dim_customers'


-- Dimensions Exploration
-- Identifying the unique values (categories) in each dimension
-- Helps recognise how data might be grouped or segmented


-- Explore all the countries our customer come from
SELECT DISTINCT Country FROM gold.dim_customers

-- Explore all categories "The major divisions"
SELECT DISTINCT Category, subcategory, product_name 
FROM gold.dim_products
ORDER BY 1,2,3

-- Date Exploration
-- Identify the earliest and latest dates (boundaries)
-- Understand the scope of data and the timespan

-- Finding the earliest and latest dates
SELECT 
MIN(order_date) first_order_date, 
MAX(order_date) last_order_date,
DATEDIFF(year, MIN(order_date), MAX(order_date)) order_range_years
FROM gold.fact_sales

-- Finding the youngest and oldest customers
SELECT 
MIN(birthdate) oldest_birthdate, 
MAX(birthdate) youngest_birthdate,
DATEDIFF(year, MIN(birthdate), GETDATE()) oldest_age,
DATEDIFF(year, MAX(birthdate), GETDATE()) youngest_age
FROM gold.dim_customers


-- Measures Exploration
-- Calculate the key metrics of our business (the big numbers)
-- Highest level of aggregation, lowest level of details

-- Find the total sales
SELECT SUM(sales_amount) TotalSales FROM gold.fact_sales 

-- Find how many items are sold
SELECT SUM(quantity) TotalProductsSold FROM gold.fact_sales

-- Find the average selling price
SELECT AVG(price) AvgSellingPrice FROM gold.fact_sales 

-- Find the total number of orders
SELECT COUNT(order_number) TotalOrders FROM gold.fact_sales
SELECT COUNT(DISTINCT order_number) TotalOrders FROM gold.fact_sales

-- Find the total number of products
SELECT COUNT(product_id) TotalProducts FROM gold.dim_products

-- Find the total number of customers
SELECT COUNT(customer_id) TotalCustomers FROM gold.dim_customers

-- Find the total number of customers that have placed an order
SELECT COUNT(DISTINCT customer_key) TotalCustomersOrderPlaced FROM gold.dim_customers dc 
WHERE EXISTS(SELECT 1 FROM gold.fact_sales fs WHERE dc.customer_key = fs.customer_key)

SELECT COUNT(DISTINCT customer_key) TotalCustomersOrderPlaced FROM gold.fact_sales

