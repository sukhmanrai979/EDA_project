-- Top 10 Customers who have generated the highest revenue

SELECT TOP 10 c.customer_key, c.first_name, c.last_name, SUM(f.sales_amount) TotalRevenue FROM gold.fact_sales f
LEFT JOIN gold.dim_customers c
ON f.customer_key = c.customer_key
GROUP BY 
c.customer_key,
c.first_name,
c.last_name
ORDER BY TotalRevenue DESC

-- 3 Customers with fewest orders placed

